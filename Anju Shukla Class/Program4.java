public class Program4 {
    public static void main(String[] args) {
        //Before swapping
        int a=10,b=20;
        System.out.println("The numbers before swapping are a="+a+" and b="+b);

        //Swapping
        int c=a;
        a=b;
        b=c;

        //After swapping
        System.out.println("The numbers after swapping are a="+a+" and b="+b);

    }
}
